    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        //
        $p_incarts = Cart::where('user_id',$request->user()->id)->get();
        $carts = array();
        foreach ($p_incarts as $prod) {
            array_push($carts,$prod->product);
        }
        
        return view('UsersSide.checkout',["carts"=>$carts]);           
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $requestedProduct = Cart::where('product_id',$request->id)->get();
        if(!$requestedProduct->isEmpty())
            return true;
        $cart = new Cart();
        $cart->user_id=$request->user()->id;
        $cart->product_id =$request->id;
        $cart->save();
        
    }

